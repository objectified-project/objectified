"""
API tests for change-report template endpoints (CR-03, #2701).

Covers: list, create, set tenant default, set project default —
auth requirements, tenant scoping, semver 409, and clearing defaults.
"""

from datetime import datetime, timezone
from unittest.mock import patch

import pytest
from fastapi.testclient import TestClient

from app.auth import validate_authentication
from app.main import app

client = TestClient(app)

# ---------------------------------------------------------------------------
# Shared auth fixtures
# ---------------------------------------------------------------------------

_JWT_ADMIN = {
    "tenant_id": "t1",
    "user_id": "admin-user",
    "auth_method": "jwt",
}

_JWT_NON_ADMIN = {
    "tenant_id": "t1",
    "user_id": "plain-user",
    "auth_method": "jwt",
}

_API_KEY = {
    "tenant_id": "t1",
    "tenant_slug": "tn",
    "auth_method": "api_key",
}

# ---------------------------------------------------------------------------
# Shared data fixtures
# ---------------------------------------------------------------------------

_TEMPLATE_SUMMARY = {
    "id": "tmpl-1",
    "semver": "1.0.0",
    "owner_tenant_id": "t1",
    "created_at": datetime(2026, 4, 14, 12, 0, 0, tzinfo=timezone.utc),
}

_TEMPLATE_FULL_ROW = {
    **_TEMPLATE_SUMMARY,
    "header_template": "# {{productName}}",
    "body_template": "## Summary",
    "footnote_template": "Generator: {{generatorVersion}}",
    "created_by": "admin-user",
}

_PROJECT_ROW = {
    "id": "p1",
    "tenant_id": "t1",
    "creator_id": "admin-user",
    "name": "MyProject",
    "slug": "myproject",
    "enabled": True,
    "metadata": {},
    "change_report_template_version_id": None,
    "created_at": datetime(2026, 4, 14, 10, 0, 0, tzinfo=timezone.utc),
    "updated_at": datetime(2026, 4, 14, 10, 0, 0, tzinfo=timezone.utc),
}

_VALID_TEMPLATE_BODY = {
    "semver": "2.0.0",
    "headerTemplate": "# {{productName}}",
    "bodyTemplate": "## Body",
    "footnoteTemplate": "Generated by {{generatorVersion}}",
}


@pytest.fixture(autouse=True)
def _default_auth():
    """Default to JWT admin for all tests; individual tests may override."""
    app.dependency_overrides[validate_authentication] = lambda: _JWT_ADMIN
    yield
    app.dependency_overrides.pop(validate_authentication, None)


# ===========================================================================
# LIST change-report-template-versions
# ===========================================================================


def test_list_templates_ok():
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.list_change_report_template_version_summaries.return_value = [_TEMPLATE_SUMMARY]
        r = client.get("/v1/tenants/tn/change-report-template-versions")
    assert r.status_code == 200
    items = r.json()
    assert len(items) == 1
    assert items[0]["id"] == "tmpl-1"
    assert items[0]["semver"] == "1.0.0"


def test_list_templates_empty():
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.list_change_report_template_version_summaries.return_value = []
        r = client.get("/v1/tenants/tn/change-report-template-versions")
    assert r.status_code == 200
    assert r.json() == []


def test_list_templates_scoped_to_caller_tenant():
    """DB layer is called with the tenant_id from the JWT, not from the URL slug."""
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.list_change_report_template_version_summaries.return_value = []
        client.get("/v1/tenants/some-other-slug/change-report-template-versions")
        mdb.list_change_report_template_version_summaries.assert_called_once_with("t1")


