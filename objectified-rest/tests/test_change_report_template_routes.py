def test_create_template_ok():
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.is_user_tenant_admin.return_value = True
        mdb.insert_change_report_template_version.return_value = _TEMPLATE_FULL_ROW
        r = client.post(
            "/v1/tenants/tn/change-report-template-versions",
            json=_VALID_TEMPLATE_BODY,
        )
    assert r.status_code == 200
    body = r.json()
    assert body["semver"] == "1.0.0"
    assert "headerTemplate" in body


def test_create_template_requires_jwt():
    """API-key callers must be rejected with 403."""
    app.dependency_overrides[validate_authentication] = lambda: _API_KEY
    r = client.post(
        "/v1/tenants/tn/change-report-template-versions",
        json=_VALID_TEMPLATE_BODY,
    )
    assert r.status_code == 403


def test_create_template_non_admin_forbidden():
    """Non-admin JWT must be rejected with 403."""
    app.dependency_overrides[validate_authentication] = lambda: _JWT_NON_ADMIN
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.is_user_tenant_admin.return_value = False
        r = client.post(
            "/v1/tenants/tn/change-report-template-versions",
            json=_VALID_TEMPLATE_BODY,
        )
    assert r.status_code == 403


def test_create_template_invalid_semver_400():
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.is_user_tenant_admin.return_value = True
        r = client.post(
            "/v1/tenants/tn/change-report-template-versions",
            json={**_VALID_TEMPLATE_BODY, "semver": ""},
        )
    assert r.status_code == 400


def test_create_template_invalid_mustache_400():
    """A syntactically invalid Mustache template must return 400."""
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.is_user_tenant_admin.return_value = True
        r = client.post(
            "/v1/tenants/tn/change-report-template-versions",
            json={**_VALID_TEMPLATE_BODY, "headerTemplate": "{{unclosed"},
        )
    assert r.status_code == 400


def test_create_template_duplicate_semver_409():
    """Unique-constraint violation from DB must surface as 409."""
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.is_user_tenant_admin.return_value = True
        mdb.insert_change_report_template_version.side_effect = Exception(
            "duplicate key value violates unique constraint (23505)"
        )
        r = client.post(
            "/v1/tenants/tn/change-report-template-versions",
            json=_VALID_TEMPLATE_BODY,
        )
    assert r.status_code == 409
    assert "already exists" in r.json()["detail"].lower()


def test_create_template_scoped_to_caller_tenant():
    """insert call uses tenant_id from JWT, not URL."""
    with patch("app.change_report_template_routes.db") as mdb:
        mdb.is_user_tenant_admin.return_value = True
        mdb.insert_change_report_template_version.return_value = _TEMPLATE_FULL_ROW
        client.post(
            "/v1/tenants/some-other-slug/change-report-template-versions",
            json=_VALID_TEMPLATE_BODY,
        )
        call_kwargs = mdb.insert_change_report_template_version.call_args
        assert call_kwargs.kwargs["tenant_id"] == "t1"


